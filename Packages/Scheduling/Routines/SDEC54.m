 .S WLDATA=$G(@RET@(COUNT))
 .S WLDATA=$P(WLDATA,$C(30),1)
 .S WLDATA=$P(WLDATA,$C(31),1)
 .Q:WLDATA=""
 .;get disposition data, if any
 .;S DISPDT=$P($G(^SDWL(409.3,WLIEN,"DIS")),U,1)
 .;S DISPU=$P($G(^SDWL(409.3,WLIEN,"DIS")),U,2)
 .;        1       2               3                4                5                 6
